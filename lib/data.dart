class IdiomDataStore {
  static String getRandomIdiom(int seed) {
    Random random = Random(seed);
    int index = random.nextInt(_popularIdioms.length);
    return _popularIdioms[index];
  }

  static List<String> getKanjis(List<String> filter) {
    if (filter.isEmpty) return _kanjis;
    return _kanjis.where((kanji) => filter.contains(kanji)).toList();
  }

  static bool isInIdioms(String target) {
    return _idioms.contains(target);
  }
}

